    "   step 4 with the critique inlined into the writer's task. Do",
    "   NOT call `task(pedagogy_critic, ...)` again on the same",
    "   target in the same user turn to verify the writer's fix —",
    "   the user verifies it in the next message.",
    "",
    "Critiques do NOT emit an artifact card — they're conversational",
